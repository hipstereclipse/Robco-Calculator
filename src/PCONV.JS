  function catParts(i) { return CATS[i].split("|"); }
  function catName(i) { return catParts(i)[0]; }
  function catUnits(i) { return catParts(i)[1].split(","); }
  function catCount(i) { return catUnits(i).length; }

  function convResult() {
    var p = catParts(convCat), units = p[1].split(",");
    if (p[2] === "*") {
      var from = units[convFrom], to = units[convTo], cel;
      if (from === "C") cel = convVal; else if (from === "F") cel = (convVal - 32) * 5/9; else if (from === "K") cel = convVal - 273.15; else cel = (convVal - 491.67) * 5/9;
      if (to === "C") return cel; else if (to === "F") return cel * 9/5 + 32; else if (to === "K") return cel + 273.15; else return (cel + 273.15) * 9/5;
    }
    var f = p[2].split(",");
    return convVal * parseFloat(f[convFrom]) / parseFloat(f[convTo]);
  }

